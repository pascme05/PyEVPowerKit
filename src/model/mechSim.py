def mechSim(data, dataTime, iter, setupMdl, setupPar):
    ###################################################################################################################
    # Calculation
    ###################################################################################################################
    # ==============================================================================
    # GBX
    # ==============================================================================
    dataTime = mechSimGBX(data, dataTime, iter, setupMdl, setupPar, 'F')
    dataTime = mechSimGBX(data, dataTime, iter, setupMdl, setupPar, 'R')

    # ==============================================================================
    # EMA
    # ==============================================================================
    dataTime = mechSimEMA(data, dataTime, iter, setupMdl, setupPar, 'F')
    dataTime = mechSimEMA(data, dataTime, iter, setupMdl, setupPar, 'R')

    return dataTime


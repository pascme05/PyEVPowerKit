import plotly.graph_objects as go
from plotly.subplots import make_subplots

#######################################################################################################################
# Additional Functions
#######################################################################################################################


#######################################################################################################################
# Main Function
#######################################################################################################################
def plotEMA(data, dataTime, setup):
    ###################################################################################################################
    # MSG IN
    ###################################################################################################################
    print("INFO: Plotting EMA data")

    ###################################################################################################################
    # Initialisation
    ###################################################################################################################
    # ==============================================================================
    # Variables
    # ==============================================================================
    time = data['t']

    ###################################################################################################################
    # Pre-Processing
    ###################################################################################################################
    fig = make_subplots(rows=3, cols=3, shared_xaxes=True, vertical_spacing=0.05)

    ###################################################################################################################
    # Calculation
    ###################################################################################################################
    # ==============================================================================
    # Plotting
    # ==============================================================================
    # ------------------------------------------
    # Mechanical
    # ------------------------------------------
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['M'], mode='lines', line=dict(color='#636EFA', dash='solid'), name='EMA Torque'), row=1, col=1)
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['n'], mode='lines', line=dict(color='#636EFA', dash='solid'), name='EMA Speed'), row=2, col=1)
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['Pm'], mode='lines', line=dict(color='#636EFA', dash='solid'), name='EMA Power'), row=3, col=1)

    # ------------------------------------------
    # Losses
    # ------------------------------------------
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['Is'], mode='lines', line=dict(color='#EF553B', dash='solid'), name='EMA Currents'), row=1, col=2)
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['Vs'], mode='lines', line=dict(color='#EF553B', dash='solid'), name='EMA Voltages'), row=2, col=2)
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['lam'], mode='lines', line=dict(color='#EF553B', dash='solid'), name='EMA Fluxes'), row=3, col=2)

    # ------------------------------------------
    # Thermal
    # ------------------------------------------
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['Pv'], mode='lines', line=dict(color='#00CC96', dash='solid'), name='Total Losses'), row=1, col=3)
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['eta'], mode='lines', line=dict(color='#00CC96', dash='solid'), name='Total Efficiency'), row=2, col=3)
    fig.add_trace(go.Scatter(x=time, y=dataTime['EMA']['T']['Pm'], mode='lines', line=dict(color='#00CC96', dash='solid'), name='Temperature'), row=3, col=3)

    ###################################################################################################################
    # Post-Processing
    ###################################################################################################################
    # ==============================================================================
    # Axis
    # ==============================================================================
    # ------------------------------------------
    # Set y-axis titles
    # ------------------------------------------
    # Mechanics
    fig.update_yaxes(title_text="M (Nm)", row=1, col=1)
    fig.update_yaxes(title_text="n (1/s)", row=2, col=1)
    fig.update_yaxes(title_text="P (W)", row=3, col=1)

    # Losses
    fig.update_yaxes(title_text="I (A)", row=1, col=2)
    fig.update_yaxes(title_text="V (V)", row=2, col=2)
    fig.update_yaxes(title_text="Lam (Vs)", row=3, col=2)

    # Thermal
    fig.update_yaxes(title_text="Pv (W)", row=1, col=3)
    fig.update_yaxes(title_text="Eta (%)", row=2, col=3)
    fig.update_yaxes(title_text="T (degC)", row=3, col=3)

    # ------------------------------------------
    # Set x-axis title for the last subplot
    # ------------------------------------------
    fig.update_xaxes(title_text="time (sec)", row=3, col=1)
    fig.update_xaxes(title_text="time (sec)", row=3, col=2)
    fig.update_xaxes(title_text="time (sec)", row=3, col=3)

    # ==============================================================================
    # Title
    # ==============================================================================
    txt = "Machine Mechanics, Electrical, Losses, and Thermal (Front + Rear): "
    fig.update_layout(height=setup['Exp']['hFig'], width=setup['Exp']['wFig'], title_text=txt)

    # ==============================================================================
    # Plot
    # ==============================================================================
    fig.show()

    ###################################################################################################################
    # Return
    ###################################################################################################################
    return []

